/* Muchas actividades humanas requieren que a diferentes coleccions de elementos utilizados
 se pongan en un orden específico. Por ello, una de las tareas mas frecuentes de las computadoras es la ordenacion.
 El estudio de los metodos de ordenacion es una tarea interesante teorica y practicamente.

 La ordenacion de datos (sort) en ingles es una operacion consistente en disponer un conjunto de datos en
 un orden determinado.

 Una coleccion de datos puede ser almacenada en memoria central o en archivos de datos externos guardados en unidades
 de almacenamiento. Cuando se guardan en memoria principal se denomina ordenacion interna; estos datos se almacenan
 exclusivamente para tratamientos internos que se utiliszan pra gestion masiva de datos y se guardan en array de una o varias dimensiones.
 Si los datos estan almacenados en un archivo, el proceso de ordenacion se llama externa.
 Este capitulo se centra en ordenacion interna.

 Todos los metodos del capitlo se basan en modo ascendente sobre listas (arreglos unidimensionales). Hay 2 grupos:

 Directos (basicos) - burbuja, selecion, insercion
 Indirectos (avanzados) - shell, quick sort, merge sort, radix sort

 En caso de listas pequeñas, los metoos directos son eficientes sobre todo porque los algoritmos son sencillos.
 Se usan mucho. Sin embargo, en listas grandes son ineficaces y es preciso recurrir a metodos avanzados. */